// READ-ONLY: dump Current RMS country list -> {id, name, code} so we can seed
// src/lib/countries.ts. The country dropdown on /contact submits to RMS using
// these internal numeric IDs (not ISO codes). Re-run if RMS adds a country.
//
// Run: node --env-file-if-exists=.env.local scripts/probe-rms-countries.mjs

const SUBDOMAIN = process.env.CURRENT_RMS_SUBDOMAIN;
const API_KEY = process.env.CURRENT_RMS_API_KEY;
if (!SUBDOMAIN || !API_KEY) {
  console.error("Missing CURRENT_RMS_SUBDOMAIN or CURRENT_RMS_API_KEY");
  process.exit(1);
}

const all = [];
let page = 1;
while (true) {
  const url = new URL("https://api.current-rms.com/api/v1/countries");
  url.searchParams.set("page", String(page));
  url.searchParams.set("per_page", "100");
  const res = await fetch(url, {
    headers: {
      "X-SUBDOMAIN": SUBDOMAIN,
      "X-AUTH-TOKEN": API_KEY,
      "Content-Type": "application/json",
    },
  });
  if (!res.ok) {
    console.error(`HTTP ${res.status} ${res.statusText}`);
    const body = await res.text().catch(() => "");
    console.error(body.slice(0, 400));
    process.exit(1);
  }
  const json = await res.json();
  const items = json.countries ?? [];
  all.push(...items);
  if (items.length < 100) break;
  page++;
}

all.sort((a, b) => a.name.localeCompare(b.name));

const lines = all.map((c) =>
  `  { id: ${c.id}, name: ${JSON.stringify(c.name)}, code: ${JSON.stringify(c.code)} },`
);

console.log(`// ${all.length} countries  (generated by scripts/probe-rms-countries.mjs)`);
console.log("export const COUNTRIES = [");
for (const l of lines) console.log(l);
console.log("] as const;\n");
console.log(`export type Country = (typeof COUNTRIES)[number];`);
